package JavaSessions;

public class DescendingNumbers {

	public static void main(String[] args) {
		
		for(int i=10;i>=1;i--) {
			System.out.println(i);
		}
		System.out.println("----------");
		
		int j=10;
		while(j>=1) {
			System.out.println(j);
			j--;
		}
		System.out.println("-----------");
		
		int k=10;
		do {
			System.out.println(k);
			k--;
		}while(k>=1);
	}
}
