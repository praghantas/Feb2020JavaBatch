package JavaSessions;

public class PositiveNegativeNum {

	public static void main(String[] args) {
		
		int a= 35;
		int b= -11;
		//to check the given number is positive or negative we use conditional operations
		
		if(a>0) 
			System.out.println("the given number "+ a + " is positive");
		else
			System.out.println("the given number "+ a+ " is negative");
		
		if(b>0)
			System.out.println("the given number "+ b + " is positive");
		else
			System.out.println("the given number "+ b + " is negative");
	}

}
